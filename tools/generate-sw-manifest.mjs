// ===========================================================================
//  generate-sw-manifest.mjs — lists every file the offline PWA should
//  cache, so service-worker.js never needs a hand-typed (and easily stale)
//  file list.
//
//  Run it from the tools/ folder whenever new assets are added (a new
//  area's sprites, a new tileset, a new src/ file) — never in the browser:
//      cd tools && node generate-sw-manifest.mjs
//
//  OUTPUT: ../sw-precache-list.js — a plain `const PRECACHE_URLS = [...]`
//  that service-worker.js loads with importScripts(). Paths are relative to
//  the repo root (same rule as everywhere else in this project — GitHub
//  Pages serves from /fakeamon_spark/, so no leading slash).
// ===========================================================================

import { writeFileSync, readdirSync, statSync } from "node:fs";
import { join, dirname, relative } from "node:path";
import { fileURLToPath } from "node:url";

const TOOLS = dirname(fileURLToPath(import.meta.url));
const ROOT = join(TOOLS, "..");

// Folders the RUNNING game never fetches, so there's no point caching them
// for offline play — they'd just bloat the cache. Update this if that ever
// stops being true (e.g. "back" sprites get wired into a real feature).
const SKIP_PREFIXES = [
  "assets/sprites/battle/", // raw vendored sheets — slicer input only (tools/slice-sheets.mjs)
  "assets/sprites/back/",   // sliced but not used by any code path yet
];

// Walk a folder and return every file inside it (relative to ROOT), skipping
// dev-only stuff that the game itself never fetches.
function walk(dir) {
  const files = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) {
      files.push(...walk(full));
    } else {
      const rel = relative(ROOT, full).split("\\").join("/"); // forward slashes on any OS
      if (!SKIP_PREFIXES.some((prefix) => rel.startsWith(prefix))) files.push(rel);
    }
  }
  return files;
}

const urls = [
  "./",
  "index.html",
  "manifest.webmanifest",
  ...walk(join(ROOT, "src")),
  ...walk(join(ROOT, "assets")),
];

const fragment = [
  "// GENERATED by tools/generate-sw-manifest.mjs — do not hand-edit.",
  "// Re-run that script after adding new assets, then commit this file too.",
  "const PRECACHE_URLS = [",
  ...urls.map((u) => `  ${JSON.stringify(u)},`),
  "];",
  "",
].join("\n");

writeFileSync(join(ROOT, "sw-precache-list.js"), fragment);
console.log(`Wrote sw-precache-list.js with ${urls.length} URLs.`);
